package HR;

public class Salary {
	private float basic;
	private float hra;
	private float lta;
	private float bonus;
	public float getBasic() {
		return basic;
	}
	public void setBasic(float basic) {
		this.basic = basic;
	}
	public float getHra() {
		return hra;
	}
	public void setHra(float hra) {
		this.hra = hra;
	}
	public float getLta() {
		return lta;
	}
	public void setLta(float lta) {
		this.lta = lta;
	}
	public float getBonus() {
		return bonus;
	}
	public void setBonus(float bonus) {
		this.bonus = bonus;
	}

}
