package HR;

public class Performance {
	private int year;
	private int attendenceRating;
	private int deliveryRating;
	
	public int getYear() {
		return year;
	}
	
	public void setYear(int year) {
		this.year = year;
	}
	
	public int getAttendenceRating() {
		return attendenceRating;
	}
	
	public void setAttendenceRating(int attendenceRating) {
		this.attendenceRating = attendenceRating;
	}
	
	public int getDeliveryRating() {
		return deliveryRating;
	}
	
	public void setDeliveryRating(int deliveryRating) {
		this.deliveryRating = deliveryRating;
	}

	
}
